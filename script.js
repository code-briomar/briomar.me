function bindMediaFallbackEvents(plateElement) {
  const img = plateElement.querySelector('.mockup-screen img');
  const video = plateElement.querySelector('.mockup-screen video');
  const fallback = plateElement.querySelector('.visual-fallback');

  if (img) {
    img.onload = () => {
      fallback.style.display = 'none';
      img.style.display = 'block';
    };
    img.onerror = () => {
      fallback.style.display = 'flex';
      img.style.display = 'none';
    };
    // Force triggering load check
    if (img.src) {
      if (img.complete) {
        fallback.style.display = 'none';
        img.style.display = 'block';
      }
    }
  }

  if (video) {
    video.addEventListener('canplay', () => {
      fallback.style.display = 'none';
      video.style.display = 'block';
    });
    video.addEventListener('error', () => {
      fallback.style.display = 'flex';
      video.style.display = 'none';
    }, true);
    
    // Trigger video load if it has src
    if (video.src) {
      video.load();
    }
  }
}

