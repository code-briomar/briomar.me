document.addEventListener("DOMContentLoaded", function () {
  const container = document.getElementById("plates");

  // 1. Flatten all projects and sort by year descending, then date order
  const allProjects = [];
  Object.entries(timelineData).forEach(([year, entries]) => {
    entries.forEach(entry => {
      allProjects.push({ ...entry, year: parseInt(year) });
    });
  });

  // Extract featured and archive projects
  const featuredProjects = allProjects.filter(p => p.featured);
  const archiveProjects = allProjects.filter(p => !p.featured);

  // 2. Render Featured Plates
  featuredProjects.forEach((entry, i) => {
    const section = document.createElement("section");
    section.className = "plate featured-plate";
    section.innerHTML = generateFeaturedPlateHTML(entry, i);
    container.appendChild(section);
    bindMediaFallbackEvents(section);
  });

  // 3. Render Archive Plate
  const archiveSection = document.createElement("section");
  archiveSection.className = "plate archive-plate";
  archiveSection.innerHTML = `
    <div class="archive-header">
      <h2 class="archive-title">Project Archive</h2>
      <p class="archive-subtitle">Chronological collection of tools, APIs, and CLI experiments (2021—2026)</p>
    </div>
    <div class="archive-grid-container">
      <div class="archive-grid">
        ${archiveProjects.map(generateArchiveCardHTML).join('')}
      </div>
    </div>
  `;
  container.appendChild(archiveSection);

  // 4. Setup Pagination and Autoplay Observers
  const plates = Array.from(document.querySelectorAll('.plate'));
  const counter = document.querySelector('.counter');
  const prevBtn = document.querySelector('.arrow-left');
  const nextBtn = document.querySelector('.circle-next');
  let current = 0;

  function updateCounter() {
    counter.textContent = `${current + 1}/${plates.length}`;
  }

  function goTo(index) {
    if (index < 0 || index >= plates.length) return;
    current = index;
    plates[current].scrollIntoView({ behavior: 'smooth' });
    updateCounter();
  }

  prevBtn.addEventListener('click', () => goTo(current - 1));
  nextBtn.addEventListener('click', () => goTo(current + 1));

  // IntersectionObserver for tracking current slide and toggling autoplay videos
  const observerOptions = {
    threshold: 0.5
  };

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        current = plates.indexOf(entry.target);
        updateCounter();

        // Autoplay video in the active slide
        const video = entry.target.querySelector('video');
        if (video) {
          video.play().catch(err => {
            console.log("Autoplay was prevented, showing fallback or awaiting user interaction:", err);
          });
        }
      } else {
        // Pause video in the inactive slide
        const video = entry.target.querySelector('video');
        if (video) {
          video.pause();
        }
      }
    });
  }, observerOptions);

  plates.forEach(p => observer.observe(p));
  updateCounter();

  // 5. Setup About Overlay Interaction
  const contactBadge = document.getElementById('contact-badge-btn');
  const aboutOverlay = document.getElementById('about-overlay');
  const closeOverlay = document.querySelector('.close-overlay');

  if (contactBadge && aboutOverlay && closeOverlay) {
    contactBadge.addEventListener('click', (e) => {
      e.preventDefault();
      aboutOverlay.classList.add('active');
      aboutOverlay.setAttribute('aria-hidden', 'false');
    });

    closeOverlay.addEventListener('click', () => {
      aboutOverlay.classList.remove('active');
      aboutOverlay.setAttribute('aria-hidden', 'true');
    });

    // Close on overlay backdrop click
    aboutOverlay.addEventListener('click', (e) => {
      if (e.target === aboutOverlay || e.target.classList.contains('overlay-content')) {
        aboutOverlay.classList.remove('active');
        aboutOverlay.setAttribute('aria-hidden', 'true');
      }
    });

    // Close on Escape key
    document.addEventListener('keydown', (e) => {
      if (e.key === 'Escape' && aboutOverlay.classList.contains('active')) {
        aboutOverlay.classList.remove('active');
        aboutOverlay.setAttribute('aria-hidden', 'true');
      }
    });
  }
});
